    <div class="item-content">
      <p class="text-item text-justify">La única Iglesia del Perú y posiblemente una de las pocas en el mundo en que se venera una santa de cuerpo presente, es la iglesia de Santo Domingo en Moquegua. Lugar donde reposa y es exhibido el cuerpo de Santa Fortunata en una urna de cristal, para la veneración de sus fieles. Dentro de esta urna se encuentra aún el cáliz que contiene la sangre de la santa y que según afirmaciones de sus devotos se ha licuado en varias oportunidades.
Cuenta la tradición que la Santa decidió permanecer en Moquegua cuando estando en el Vaticano su cuerpo se hizo ligero como una pluma al ser mencionado el nombre de la antigua Villa de “Santa Catalina de Guadalcázar del Valle de Moquegua” y tan pesado cuando fue desembarcado en el puerto de Ilo, haciendo imposible su retorno al barco. 
El misticismo hacia Santa Fortunata es solemne. Se le atribuyen muchos milagros y según la tradición ella dio aviso sobre la suerte de Moquegua, a través de milagrosos prodigios, tales como el crecimiento de sus uñas y cabello. Según el testimonio de nuestros ancestros, antes que sitiara la ciudad el ejército chileno a manos del Comandante Salvo, la Santa comenzó a sudar. Dice la profecía que el día que su cuerpo sea removido de la ciudad, Moquegua desaparecerá. 
